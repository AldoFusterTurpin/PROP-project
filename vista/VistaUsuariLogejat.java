	public void slotPartida(int slot){
		CtrlPersistencia pers=new CtrlPersistencia();
		if(pers.llistaSavedGames(usernameLogejat).contains(String.valueOf(slot))){
			Partida p=pers.carregarPartida(usernameLogejat,slot);
			pers.eliminarPartida(usernameLogejat, slot);
                        VistaPartida.main(p,ctrlDomini);
		}
		else {
			JOptionPane.showMessageDialog(null,"No hi ha cap partida en el slot seleccionat","Alert",JOptionPane.ERROR_MESSAGE);
		}
	}
