    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        Object[] options = { "Yes", "No" };
        int n = JOptionPane.showOptionDialog(null,
            "Are you sure you want to delete your profile? You will lost all your data including current games", //->missatge que es mostrarà
            "Warning", //->títol del cuadre de dialeg que es mostrarà
            JOptionPane.YES_NO_CANCEL_OPTION, //tipus d'opcions mostrades
            JOptionPane.WARNING_MESSAGE, //icona de 'question'(pregunta)
            null, //no usar icona personalitzat
            options, //els titols dels butons
            options[1]); //quin té el focus(iluminat)

        if (n == JOptionPane.YES_OPTION) {
            try {

                String id = ctrlDomini.obtenirIdDeUsername(usernameLogejat);
                ctrlDomini.eliminarJugadorRegistrat(id);
                ctrlDomini.guardarJugadors();
                dispose();
            } catch (Exception ex) {
                Logger.getLogger(VistaUsuariLogejat.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_jButton4ActionPerformed

